    private void jcboTipoFacturaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcboTipoFacturaItemStateChanged
        // maneja el evento de cambios en la seleccion del combo box de vehiculos
        try{   
            // verifica si el evento disparado es una selecccion
            if (evt.getStateChange() == ItemEvent.SELECTED) {
                // Obtiene el index seleccionado en el combo box de tipo de factura
                int index = jcboVehiculo.getSelectedIndex();

                // guarda en una variable vehiculo el cliente seleccionado en el 
                //combo box
                TipoFactura tf = (TipoFactura)jcboTipoFactura.getSelectedItem();
                
                //Verica que se haya selccionado el vehiculo
                if(index!=-1){
                    // se estblece el nuevo cliente
                    f.setTipoFactura(tf);
                    
                    // Despliega los detalles de la factura en el text area
                    jtxaDetalles.setText(f.toString());
                }
            }
        }
        catch(Exception e){ 
        }
    }//GEN-LAST:event_jcboTipoFacturaItemStateChanged

