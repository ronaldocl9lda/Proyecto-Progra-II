    private void cargarCombos(){
        // Se itera a traves de los tipos de los clientes y se agregan al combo box
        for (Cliente c:PersistenciaCliente.getArrayClientesActivos()){
            jcboCliente.addItem(c);
        }
        
        // Se itera a traves de los tipos de vehiculo y se agregan al combo box
        // tipo vehiculo
        for (TipoVehiculo tipoVehiculo:TipoVehiculo.values()){
            jcboTipoVehiculo.addItem(tipoVehiculo);
        }
        
        // Se itera a traves de los tipos de factura y se agregan al combo box
        for (TipoFactura tipoFactura:TipoFactura.values()){
            this.jcboTipoFactura.addItem(tipoFactura);
        }
    }
    
