    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        for (Vehiculo vehiculo : PersistenciaVehiculo.getArrayVehiculos()) { //foreach -recorre cada celda del arreglo, uno a la vez
            JCbxSeleccioneVehículo.addItem(vehiculo);//agrega el vehiculo al combo box
        }
        
        for (TipoVehiculo tipoVehiculo : TipoVehiculo.values()) { //foreach -recorre cada celda del arreglo, uno a la vez
            JCbxTipoVehiculo.addItem(tipoVehiculo);
        }
        
        JCbxSeleccioneVehículo.setSelectedIndex(-1);
        this.JTxfModelo.setText("");
        this.JTxfPrecio.setText("");
        JCbxTipoVehiculo.setSelectedIndex(-1);
        
    }//GEN-LAST:event_formWindowOpened

    private void JCbxSeleccioneVehículoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_JCbxSeleccioneVehículoItemStateChanged
        try{
            if (evt.getStateChange() == ItemEvent.SELECTED) {
                Vehiculo v = (Vehiculo)JCbxSeleccioneVehículo.getSelectedItem();
                this.JTxfModelo.setText(v.getModelo());
                this.JTxfPrecio.setText(String.valueOf(v.getPrecio()));
                this.JCbxTipoVehiculo.setSelectedItem(v.getTipoVehiculo());
            }
        }
        catch(Exception e){ 
        }
    }//GEN-LAST:event_JCbxSeleccioneVehículoItemStateChanged

