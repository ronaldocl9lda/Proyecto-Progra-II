    private void JBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnGuardarActionPerformed
        if(JCbxSeleccioneVehículo.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Seleccione un vehiculo");//despliega un mensaje de error
            JCbxSeleccioneVehículo.requestFocus();//solicita qque lo vuelva a intentar
            return;
        }
        
        if (JTxfModelo.getText().trim().equals("")){ //Se abre condicion if que dará instrucciones en caso de que jTxtDireccion no tenga nada escrito
            JOptionPane.showMessageDialog(this, "Debe digitar el modelo"); //Por medio de un JOptionPane, se mostrará un mensaje de alerta
            JTxfModelo.requestFocus(); //El programa se enfocará en el jTxtDireccion para editarlo
            return; //Se cierra la condición
        }
          if (JTxfPrecio.getText().trim().equals("")){ //Se abre condicion if que dará instrucciones en caso de que jTxtDireccion no tenga nada escrito
            JOptionPane.showMessageDialog(this, "Debe digitar el precio"); //Por medio de un JOptionPane, se mostrará un mensaje de alerta
            JTxfPrecio.requestFocus(); //El programa se enfocará en el jTxtDireccion para editarlo
            return; //Se cierra la condición
        }
          
        try {
            double precio2 = Double.parseDouble(this.JTxfPrecio.getText());// crea la variable 
        } catch (NumberFormatException numberFormatException) {//define el error que quiere atrapar
            JOptionPane.showMessageDialog(this, "El precio no debe tener letras");//confirma el error
            JTxfPrecio.setText("");
            JTxfPrecio.requestFocus();
            JTxfPrecio.selectAll();
            return;
        }
        
        int index = JCbxTipoVehiculo.getSelectedIndex();
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Seleccione el tipo de vehiculo");//despliega un mensaje de error
            JCbxTipoVehiculo.requestFocus();//solicita qque lo vuelva a intentar
            return;
        }
        
        String modelo = JTxfModelo.getText();//otiene los tres datos para la creacion del vehiculo
        double precio= Double.parseDouble(JTxfPrecio.getText());
        TipoVehiculo tipo = (TipoVehiculo) JCbxTipoVehiculo.getSelectedItem();

        int indice =  JCbxSeleccioneVehículo.getSelectedIndex(); //Guarda en una variable indice de tipo int, el indice seleccionado del combo box
        Vehiculo vehiculo= (Vehiculo)JCbxSeleccioneVehículo.getSelectedItem();
        vehiculo.setModelo(modelo);
        vehiculo.setPrecio(precio);
        vehiculo.setTipoVehiculo(tipo);
        
        PersistenciaVehiculo.actualizarVehiculo(indice, vehiculo); //Actualiza el objeto y lo guarda en la clase PersistenciaCliente
        JOptionPane.showMessageDialog(this, "Se ha actualizado!"); //Imprime un mensaje de que la accion se ha realizado con exito
        
        JCbxSeleccioneVehículo.setSelectedIndex(-1);
        JTxfPrecio.setText("");
        JTxfModelo.setText("");
    }//GEN-LAST:event_JBtnGuardarActionPerformed

