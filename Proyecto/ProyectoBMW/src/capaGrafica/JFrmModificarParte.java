    private void JBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnAgregarActionPerformed
        if(jcboVehiculo.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this,"Seleccione un vehiculo");
            jcboVehiculo.requestFocus();
            return;
        }
        
        if(jcboParte.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this,"Seleccione una parte");
            jcboParte.requestFocus();
            return;
        }
        
        Vehiculo v = (Vehiculo)jcboVehiculo.getSelectedItem();
        Parte p = (Parte) jcboParte.getSelectedItem();
        if(v.agregarParte(p)){
            JOptionPane.showMessageDialog(this, "Parte Agregada");
            jcboParte.setSelectedIndex(-1);
            jcboParte.requestFocus();
            cargarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "La parte ya existe en el vehiculo");
            jcboParte.setSelectedIndex(-1);
            jcboParte.requestFocus();
        }
    }//GEN-LAST:event_JBtnAgregarActionPerformed

