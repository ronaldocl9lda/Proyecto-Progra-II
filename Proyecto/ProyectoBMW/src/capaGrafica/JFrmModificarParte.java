    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        for (Vehiculo v : PersistenciaVehiculo.getArrayVehiculos()) { 
            jcboVehiculo.addItem(v); 
        } //Se cierra el ciclo for each
        
        for (Parte parte : PersistenciaParte.getArrayPartesActivas()) { //Ciclo for each para llenar el combo box jCboCliente
            jcboParte.addItem(parte); //Se guarda en el jCboCliente el toString del objeto cliente
        } //Se cierra el ciclo for each
        
        jcboVehiculo.setSelectedIndex(-1);
        jcboParte.setSelectedIndex(-1); //Posiciona el jCboCliente en la posición -1 una posicion vacía
        JTxaPartesVehiculo.setText("");
    }//GEN-LAST:event_formWindowOpened

    private void JBtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnBorrarActionPerformed
        if(jcboVehiculo.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this,"Seleccione un vehiculo");
            jcboVehiculo.requestFocus();
            return;
        }
        
        if(jcboParte.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this,"Seleccione una parte");
            jcboParte.requestFocus();
            return;
        }
        
        Vehiculo v = (Vehiculo)jcboVehiculo.getSelectedItem();
        Parte p = (Parte) jcboParte.getSelectedItem();
        if(v.eliminarParte(p)){
            JOptionPane.showMessageDialog(this, "Parte Eliminada");
            jcboParte.setSelectedIndex(-1);
            jcboParte.requestFocus();
            cargarDatos();
        }else{
            JOptionPane.showMessageDialog(this, "La parte no existe en el vehiculo");
            jcboParte.setSelectedIndex(-1);
            jcboParte.requestFocus();
        }
    }//GEN-LAST:event_JBtnBorrarActionPerformed

    private void jcboVehiculoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcboVehiculoItemStateChanged
        try{
            if (evt.getStateChange() == ItemEvent.SELECTED) {
                cargarDatos();
            }
        }
        catch(Exception e){ 
        }
    }//GEN-LAST:event_jcboVehiculoItemStateChanged

    private void cargarDatos(){
        String hilera = "";
        Vehiculo v = (Vehiculo)jcboVehiculo.getSelectedItem();
        for(Parte p:v.getArrayPartes()){
            hilera += p.toString()+"\n";
        }
        this.JTxaPartesVehiculo.setText(hilera);
    }
    
